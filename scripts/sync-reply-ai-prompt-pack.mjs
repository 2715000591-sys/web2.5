#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT_DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const PACK_DIR = path.join(ROOT_DIR, "docs/ai-prompt-packs/sexual-leadgen-foundation");
const OUTPUT_PATH = path.join(ROOT_DIR, "cloudflare/src/reply-ai-prompt-pack.generated.js");
const checkOnly = process.argv.includes("--check");

function readText(filePath) {
  return fs.readFileSync(filePath, "utf8").replace(/\r\n/g, "\n").trim();
}

function readJson(filePath) {
  return JSON.parse(readText(filePath));
}

function buildGeneratedSource() {
  const readme = readText(path.join(PACK_DIR, "README.md"));
  const prompt = readText(path.join(PACK_DIR, "prompt.md"));
  const samples = readJson(path.join(PACK_DIR, "samples.json"));
  const pack = {
    id: "sexual-leadgen-foundation",
    updatedAt: readme.match(/最后整理日期：([^\n]+)/)?.[1]?.trim() || "",
    sourceDir: "docs/ai-prompt-packs/sexual-leadgen-foundation",
    prompt,
    samples
  };

  return [
    "// Generated by scripts/sync-reply-ai-prompt-pack.mjs.",
    "// Source of truth: docs/ai-prompt-packs/sexual-leadgen-foundation/.",
    "// Do not edit this file by hand.",
    "",
    `export const REPLY_AI_PROMPT_PACK = Object.freeze(${JSON.stringify(pack, null, 2)});`,
    ""
  ].join("\n");
}

const nextSource = buildGeneratedSource();
const currentSource = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, "utf8") : "";

if (checkOnly) {
  if (currentSource !== nextSource) {
    console.error("reply AI prompt pack is out of sync. Run: npm run prompt-pack:sync");
    process.exitCode = 1;
  }
} else if (currentSource !== nextSource) {
  fs.writeFileSync(OUTPUT_PATH, nextSource, "utf8");
  console.log(`Synced ${path.relative(ROOT_DIR, OUTPUT_PATH)}`);
} else {
  console.log("Reply AI prompt pack is already in sync.");
}
